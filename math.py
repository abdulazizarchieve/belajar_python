#cara melakukan penjumlahan di python
print("operasi penjumlahan (+)")
print("1 + 1 =", 1 + 1)
print("1.5 + 1.5 =", 1.5 + 1.5)
print("3.5 + 6.7 =", 3.5 + 6.7)
print("==========")

#cara melakukan pengurangan di python
print("operasi pengurangan (-)")
print("9 - 3 =", 9 - 3 )
print("1 - 3 =", 1 - 3 )
print("3.8 - 3.3 =", 3.8 - 3.3 )
print("==========")


#cara melakukan pembagian di python
print("operasi pembagian (/)")
print("9 / 3 =", 9 / 3 )
print("3 / 10 =", 3 / 10 )
print("10.5 / 2 =", 10.5 / 2 )
print("==========")

#cara melakukan perkalian di python
print("operasi perkalian (*)")
print("9 * 3 =", 9 * 3 )
print("3 * 10 =", 3 * 10 )
print("1.5 * 2 =", 1.5 * 2.5 )
print("==========")

#cara melakukan modulus di python
#modulus adalah sisa bagi dari dari sebuah operasi pembagian
# x dibagi y sisa j
print("operasi modulus (%)")
print("9 % 3 =", 9 % 3 )
print("1.5 % 2 =", 3 % 2 )
print("3.5 % 2 =", 3.5 % 2 )
print("100 % 16 =",100 % 16 )
print("==========")

#cara menggunakan lebih kecil di python
#hasil dari operasi ini akan menghasilkan True (benar) atau False (salah)
print("operasi lebih kecil (<)")
print("9 < 3 = Benar", 3 < 9 )
print("50 < 10 = Salah", 50 < 10 )
print("==========")

#cara menggunakan lebih besar di python
#hasil dari operasi ini akan menghasilkan True (benar) atau False (salah)
print("operasi pengurangan (>)")
print("30 > 9 = Benar", 30 > 9 )
print("50 > 100 = Salah", 50 > 100 )
print("==========")

#cara menggunakan lebih kecil sama dengan di python
#hasil dari operasi ini akan menghasilkan True (benar) atau False (salah)
print("operasi pengurangan (<=)")
print("30 <= 30 = Benar", 30 <= 30 )
print("51 <= 50 = Salah", 51 <= 50 )
print("49 <= 50 = Benar", 49 <= 50 )
print("==========")

#cara menggunakan lebih besar sama dengan di python
#hasil dari operasi ini akan menghasilkan True (benar) atau False (salah)
print("operasi pengurangan (<=)")
print("30 >= 30 = Benar", 30 >= 30 )
print("51 >= 50 = Benar", 51 >= 50 )
print("49 >= 50 = Salah", 49 >= 50 )
print("==========")

